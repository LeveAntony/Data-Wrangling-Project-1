"""# helper Function"""

def haversine_distance(lat1, lon1, lat2, lon2, radius=6378):
    """Calculate haversine distance between two points (in km)"""
    lat1, lon1, lat2, lon2 = map(radians, [lat1, lon1, lat2, lon2])
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    a = sin(dlat/2)**2 + cos(lat1) * cos(lat2) * sin(dlon/2)**2
    c = 2 * asin(sqrt(a))
    return radius * c

def get_season_from_date(date_str):
    """Determine Australian season from date"""
    try:
        date_obj = pd.to_datetime(date_str)
        month = date_obj.month
        if month in [12, 1, 2]:
            return 'Summer'
        elif month in [3, 4, 5]:
            return 'Autumn'
        elif month in [6, 7, 8]:
            return 'Winter'
        elif month in [9, 10, 11]:
            return 'Spring'
    except:
        return None

def calculate_sentiment_happiness(review_text):
    """Calculate customer happiness from sentiment analysis (compound >= 0.05)"""
    if pd.isna(review_text) or review_text == 'None':
        return True  # Assume happy if no previous purchase
    sia = SentimentIntensityAnalyzer()
    sentiment_scores = sia.polarity_scores(str(review_text))
    return sentiment_scores['compound'] >= 0.05

def find_nearest_warehouse(cust_lat, cust_lon, warehouse_df):
    """Find the nearest warehouse to customer coordinates"""
    min_distance = float('inf')
    nearest_warehouse = None
    for _, warehouse in warehouse_df.iterrows():
        distance = haversine_distance(cust_lat, cust_lon,
                                     warehouse['lat'], warehouse['lon'])
        if distance < min_distance:
            min_distance = distance
            nearest_warehouse = warehouse['names']
    return nearest_warehouse, round(min_distance, 4)

def calculate_order_total(order_price, coupon_discount, delivery_charges):
    """Calculate order total: price * (1 - discount/100) + delivery"""
    discounted_price = order_price * (1 - coupon_discount / 100)
    total = discounted_price + delivery_charges
    return round(total, 2)

