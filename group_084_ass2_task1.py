print(f"  Min: {delivery_charges.min():.2f}")
print(f"  Max: {delivery_charges.max():.2f}")

# Method 1: 3-Sigma Rule
print("" + "-"*70)
print("METHOD 1: 3-SIGMA RULE")
print("-"*70)
mean = delivery_charges.mean()
std = delivery_charges.std()
lower_3sigma = mean - 3 * std
upper_3sigma = mean + 3 * std

outliers_3sigma = outlier_df[
    (outlier_df['delivery_charges'] < lower_3sigma) |
    (outlier_df['delivery_charges'] > upper_3sigma)
]
print(f"  Bounds: [{lower_3sigma:.2f}, {upper_3sigma:.2f}]")
print(f"  Detected: {len(outliers_3sigma)} outliers")

# Method 2: Hampel Identifier
print("" + "-"*70)
print("METHOD 2: HAMPEL IDENTIFIER")
print("-"*70)
median = delivery_charges.median()
mad = np.median(np.abs(delivery_charges - median))
lower_hampel = median - 3 * mad
upper_hampel = median + 3 * mad

outliers_hampel = outlier_df[
    (outlier_df['delivery_charges'] < lower_hampel) |
    (outlier_df['delivery_charges'] > upper_hampel)
]
print(f"  Median: {median:.2f}, MAD: {mad:.2f}")
print(f"  Bounds: [{lower_hampel:.2f}, {upper_hampel:.2f}]")
print(f"  Detected: {len(outliers_hampel)} outliers")

# Method 3: Boxplot/IQR
print("" + "-"*70)
print("METHOD 3: BOXPLOT (IQR)")
print("-"*70)
Q1 = delivery_charges.quantile(0.25)
Q3 = delivery_charges.quantile(0.75)
IQR = Q3 - Q1
lower_iqr = Q1 - 1.5 * IQR
upper_iqr = Q3 + 1.5 * IQR

outliers_iqr = outlier_df[
    (outlier_df['delivery_charges'] < lower_iqr) |
    (outlier_df['delivery_charges'] > upper_iqr)
]
print(f"  Q1: {Q1:.2f}, Q3: {Q3:.2f}, IQR: {IQR:.2f}")
print(f"  Bounds: [{lower_iqr:.2f}, {upper_iqr:.2f}]")
print(f"  Detected: {len(outliers_iqr)} outliers")

