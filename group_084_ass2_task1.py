def _fmt(y, m, d):
    return f"{int(y):04d}-{int(m):02d}-{int(d):02d}"

def _normalize(s):
    # unify separators to hyphen; strip spaces
    return re.sub(r'[./\s]', '-', str(s).strip())

def fix_date_rule_based(s):
    """
    Apply your rules exactly:

    1) If the first two items are both <= 12 (e.g., 10-05-2018), assume DD-MM-YYYY (Melbourne),
       convert to YYYY-MM-DD.
    2) If one of the first two items > 12, use that to decide DD/MM, then convert to YYYY-MM-DD.
    3) If it's like '2019-03-04' (YYYY-MM-DD with month/day both <= 12 → ambiguous), DO NOT FIX.
       Return original.
    4) If it's '2019-28-04' (YYYY-DD-MM with middle > 12), fix to '2019-04-28'.
    """
    if s is None or (isinstance(s, float) and pd.isna(s)) or str(s).strip().lower() == 'nan':
        return s

    raw = str(s)
    ss = _normalize(raw)

    parts = ss.split('-')
    if len(parts) != 3 or not all(p.isdigit() for p in parts):
        # Not a simple 3-part numeric date: leave as-is
        return raw

    p1, p2, p3 = parts
    # Try to identify where the 4-digit year is (if any)
    year_idx = None
    for i, p in enumerate(parts):
        if len(p) == 4:
            year_idx = i
            break

    # Case A: Year is last (common for DD-MM-YYYY or MM-DD-YYYY)
    if year_idx == 2:
        dd = int(p1)
        mm = int(p2)
        yyyy = int(p3)

        if dd <= 12 and mm <= 12:
            # Rule 1: ambiguous first two -> assume DD-MM-YYYY (Melbourne)
            d, m = dd, mm
        elif dd > 12 and mm <= 12:
            # Rule 2: clearly DD-MM-YYYY
            d, m = dd, mm
        elif mm > 12 and dd <= 12:
            # Rule 2: clearly MM-DD-YYYY
            d, m = mm, dd
        else:
            # Both > 12 (invalid), leave as-is
            return raw

        # Validate and output
        if _is_valid_date(yyyy, m, d):
            return _fmt(yyyy, m, d)
        else:
            return raw

    # Case B: Year is first (possible YYYY-MM-DD or YYYY-DD-MM)
    if year_idx == 0:
        yyyy = int(p1)
        mm = int(p2)
        dd = int(p3)

        if mm <= 12 and dd <= 12:
            # Rule 3: ambiguous like 2019-03-04 → DO NOT FIX
            return raw

        if mm > 12 and dd <= 12:
            # Clearly YYYY-DD-MM → swap to YYYY-MM-DD
            m, d = dd, mm
            if _is_valid_date(yyyy, m, d):
                return _fmt(yyyy, m, d)
            else:
                return raw

        if dd > 12 and mm <= 12:
            # Looks like valid YYYY-MM-DD already (non-ambiguous)
            if _is_valid_date(yyyy, mm, dd):
                return _fmt(yyyy, mm, dd)
            else:
                return raw

        # If both > 12 or other weirdness → leave as-is
        return raw

    # Case C: Year is middle (rare: DD-YYYY-MM or MM-YYYY-DD) → use the “first-two-items” rule
    if year_idx == 1:
        first = int(p1)
        yyyy = int(p2)
        third = int(p3)

        # First-two-items = [p1, p2] but p2 is year (>12), so this falls under rule 2
        # Decide which of first/third is month/day by validity
        # Try interpretation: first is day, third is month
        if first <= 31 and third <= 12 and _is_valid_date(yyyy, third, first):
            return _fmt(yyyy, third, first)
        # Try swapped: first is month, third is day
        if first <= 12 and third <= 31 and _is_valid_date(yyyy, first, third):
            return _fmt(yyyy, first, third)
        return raw

    # No 4-digit year found → rely strictly on rule 1/2 using the first two items and treat p3 as year
    # (This is risky if p3 isn't a real 4-digit year; we keep behavior conservative.)
    if len(p3) == 4 and p3.isdigit():
        dd_or_mm_1 = int(p1)
        dd_or_mm_2 = int(p2)
        yyyy = int(p3)

        if dd_or_mm_1 <= 12 and dd_or_mm_2 <= 12:
            # Rule 1: assume DD-MM-YYYY
            d, m = dd_or_mm_1, dd_or_mm_2
        elif dd_or_mm_1 > 12 and dd_or_mm_2 <= 12:
            # Rule 2: DD-MM-YYYY
            d, m = dd_or_mm_1, dd_or_mm_2
        elif dd_or_mm_2 > 12 and dd_or_mm_1 <= 12:
            # Rule 2: MM-DD-YYYY
            d, m = dd_or_mm_2, dd_or_mm_1
        else:
            return raw

        if _is_valid_date(yyyy, m, d):
            return _fmt(yyyy, m, d)
        else:
            return raw

    # Fallback: leave unchanged
    return raw


